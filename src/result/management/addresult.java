    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String srollno=rollno.getText();
        String spy=python.getText();
        String sdbms=dbms.getText();
        String snetworks=networks.getText();
        String smaths=maths.getText();
        String sos=os.getText();
        
        try
        {
            //connection to mysql DB through JDBC
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Mysql@123");
            
            //inserted into table
            Statement st=con.createStatement();
            
            //rs will hold position 1 step back the actual query,if present
            //else rs.next()==flase
            ResultSet rs=st.executeQuery("select * from student where rollno='"+srollno+"' ");
            
            //if query is present in student table
            if(rs.next())
            {
                //insert values in result table
                st.executeUpdate("insert into result(rollno,python,dbms,networks,maths,os) values( '"+srollno+"', '"+spy+"', '"+sdbms+"', '"+snetworks+"', '"+smaths+"', '"+sos+"') ");
                
                JOptionPane.showMessageDialog(null, "Student result has saved.");
                setVisible(false);
                new addresult().setVisible(true);
                
            }
            
            //incorrect rollno
            else
            {
                JOptionPane.showMessageDialog(null, "The rollno is not registered !");
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_saveActionPerformed

