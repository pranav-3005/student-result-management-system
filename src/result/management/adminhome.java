    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String srollno = rollno.getText();
        String sname=name.getText();
        String sgender=(String)gender.getSelectedItem();
        String sfather=father.getText();
        String scourse=(String)course.getSelectedItem();  //dropdown list box returns object only
        String sdept=(String)dept.getSelectedItem();
        
        try
        {
            //connection to mysql DB through JDBC
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Mysql@123");
            
            //inserted into table
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,name,gender,fathername,course,branch) values('"+srollno+"' , '"+sname+"', '"+sgender+"', '"+sfather+"', '"+scourse+"', '"+sdept+"' )");
            
            JOptionPane.showMessageDialog(null, "Student's data saved successfully.");
            
            //new admin home(window) to add another student, if req.
            setVisible(false);
            new adminhome().setVisible(true);
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

